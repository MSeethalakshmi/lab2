package week2_book1;

public class Switchcase {
	public static void main(String[] args) {
		int i=1;
		switch(i) {
		case 0:
			System.out.println("zero");
			break;
		case 1:
			System.out.println("one");
		case 2:
			System.out.println("two");
		default:
			System.out.println("default");//Answer: One,two default
		}
	}
}
